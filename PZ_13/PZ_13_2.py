#В двумерном списке элементы строки N (N задать с клавиатуры) увеличить на 3.


# Пример двумерного списка (можно заменить на свой)
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Ввод номера строки (индекса) с клавиатуры
N = int(input("Введите номер строки (от 0 до {}): ".format(len(matrix) - 1)))

# Проверка корректности введённого номера
if 0 <= N < len(matrix):
    # Увеличиваем каждый элемент строки N на 3
    matrix[N] = [x + 3 for x in matrix[N]]
    print("Обновлённый список:")
    for row in matrix:
        print(row)
else:
    print("Ошибка: введён неверный номер строки.")