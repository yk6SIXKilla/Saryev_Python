# Книжные магазины предлагают следующие коллекции книг.
# Магистр – Лермонтов, Достоевский, Пушкин, Тютчев
# ДомКниги – Толстой, Грибоедов, Чехов, Пушкин.
# БукМаркет – Пушкин, Достоевский, Маяковский.
# Галерея – Чехов, Тютчев, Пушкин. Определить в каких магазинах
# можно приобрести книги Маяковского
magistr = {"Лермонтов", "Достоевский", "Пушкин", "Тютчев"}
dom_knigi = {"Толстой", "Грибоедов", "Чехов", "Пушкин"}
bukmarket = {"Пушкин", "Достоевский", "Маяковский"}
galeriya = {"Чехов", "Тютчев", "Пушкин"}

# Определение магазинов, где продаются книги Маяковского
mayakovskiy = "Маяковский"
magazines_with_mayakovskiy = [name for name, authors in zip(["Магистр", "ДомКниги", "БукМаркет", "Галерея"],
                                                            [magistr, dom_knigi, bukmarket, galeriya])
                              if mayakovskiy in authors]

# Вывод результатов
if magazines_with_mayakovskiy:
    print(f"Книги Маяковского можно приобрести в магазине: {magazines_with_mayakovskiy[0]}")
else:
    print("Книги Маяковского не найдены в списке магазинов.")
